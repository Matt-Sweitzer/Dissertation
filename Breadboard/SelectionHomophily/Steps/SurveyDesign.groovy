def createSurveyQuestion(q, player, randomize=false) {

  if ( (q.qtype == "radio" || q.qtype == "checkbox" || q.qtype == "select") && randomize ) {
    Collections.shuffle(q.options)
  }

  def choiceHtml = ""

  switch (q.qtype) {
    case "radio":
      q.options.each { option->
        choiceHtml += """
          <input type="radio" name="${q.name}" class="param" ng-model="${q.name}" value="${option.value}" required>
          <label for="${option.value}"> ${option.text}</label><br>
        """
      }
      break
    case "select":
      def optionHtml = ""
      q.options.each { option->
        optionHtml += """<option value="${option.value}">${option.text}</option>"""
      }

      choiceHtml += """
        <select name="${q.name}" class="param" ng-model="${q.name}" required>
          ${optionHtml}
        </select>
      """
      break
    case "numeric":
      choiceHtml += """
        <input type="number" name="${q.name}" class="param" ng-model="${q.name}" min="12" max="120" required>
      """
      break
      case "checkbox":
        q.options.each { option->
          choiceHtml += """
            <input type="checkbox" id="${q.name}" name="${q.name}" class="param" value="${option.value}">
            <label for="${option.value}"> ${option.text}</label><br>
          """
        }
        break
      case "checkbox2":
        q.options.each { option->
          choiceHtml += """
            <input type="checkbox" id="${q.name}" name="${q.name}" class="param" value="${option.value}" checked>
            <label for="${option.value}"> ${option.text}</label><br>
          """
        }
        break
      case "text":
        choiceHtml += """
          <input type="text" id="${q.name}" name="${q.name}" class="param" ng-model="${q.name}" required>
        """
        break
  }

  return  [name: "Next",
    class: "btn btn-custom1 btn-lg btn-block",
    custom: """
      <br>
      <p style="text-align: left; font-size: 16px; font-family: Serif;">${q.header}</p>
      <br>
      <p style="text-align: left; font-size: 18px; font-family: Serif;">${q.text}</p>
      <br>
      <div>
        ${choiceHtml}
      </div>
    """.toString(),
    result: { params->
